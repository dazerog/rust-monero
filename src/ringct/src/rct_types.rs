use crypto::crypto_ops::ge_dsmp;

const ATOMS: usize = 64;

pub struct Key {
    pub bytes: [char; 32],
}

impl Key {
    pub fn new(data: [u8; 32]) -> Key {
        let mut tmp: [char; 32] = [0 as char; 32];
        for (i, v) in data.iter().enumerate() {
            tmp[i] = *v as char;
        }
        let k = Key { bytes: tmp };
        k
    }
}

